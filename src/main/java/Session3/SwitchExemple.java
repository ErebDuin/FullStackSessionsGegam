package Session3;

public class SwitchExemple {
    public static void main(String[] args) {
        int numberToString = 4;
        switch (numberToString) {
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("Invalid number");
        }
    }
}
